            <p><h3>O Museu</h3></p>
            <p align="justify">O Museu Casa do Sertão é um dos órgãos responsáveis pelo desenvolvimento da política cultural da Universidade Estadual de Feira de Santana (UEFS), tendo uma atuação marcante no fomento e difusão da cultura local e no resgate, preservação e valorização da cultura popular.
            Construído no Campus Universitário, em 1978, pelo Lions Clube de Feira de Santana, que doou o espaço à Universidade, o Museu contava, inicialmente, apenas com uma sala para exposições temporárias, uma sala para a exposição do acervo permanente e uma pequena biblioteca.</p>
            <p align="justify">Ao longo dos anos, houve um notável crescimento, tanto em termos de estrutura física quanto de acervo museológico. Novas peças foram incorporadas à coleção do Museu Casa do Sertão, a exemplo do Acervo do Couro, que pertenceu ao Museu Regional de Arte de Feira de Santana (MRA), sendo transferido para a Casa do Sertão em 1995, após a vinculação do MRA à UEFS; e da biblioteca pessoal do Monsenhor Renato de Andrade Galvão, doada em vida pelo sacerdote, educador, historiador e Vice-Reitor da Universidade (1979 - 1987) pouco antes de sua morte, em 1995.</p>
            
