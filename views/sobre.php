            <p><h3>O Acervo</h3></p>
            <p align="justify">Entre 1995 e 1996, o prédio que abriga o Museu Casa do Sertão sofreu uma grande reforma, triplicando a sua área construíd. A ampliação possibilitou a redistribuição do acervo, em função da criação de novos espaços, como a Sala do Artesanato, que passou a abrigar o acervo iconográfico; a Sala Eurico Alves Boaventura, que recebeu o acervo do Ciclo do Couro; a Sala Dival da Silva Pitombo, reservada a exposições temporárias; a Sala da Administração; o Centro de Estudos Feirenses; o Núcleo de Literatura de Cordel; a Biblioteca Setorial; a Reserva Técnica e o Pavilhão Anexo, destinado às peças de grande porte.</p>
            <p align="justify">O Museu Casa do Sertão vem preservando a cultura sertaneja e resguardando aspectos do cotidiano do homem sertão. Possui uma coleção iconográfica constituída de 1.169 artefatos em couro, cerâmica, metal, madeira, fibras e matrizes de xilogravura, etc; que remetem à maneira de ser do povo nordestino. Esse acervo permanece aberto à visitação pública diariamente, em horário comercial.</p>
